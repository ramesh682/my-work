# Arithmetic Operators
a = 10
b = 3
print("Addition:", a + b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

# Assignment Operators
x = 5
x += 3  # same as x = x + 3
print("After += :", x)

# Comparison Operators
print("Is a greater than b?", a > b)

# Logical Operators
is_admin = True
is_active = False
print("Admin and Active:", is_admin and is_active)
print("Admin or Active:", is_admin or is_active)

# Membership Operators
fruits = ['apple', 'banana', 'cherry']
print("'apple' in fruits?", 'apple' in fruits)

# Identity Operators
num1 = 100
num2 = 100
print("num1 is num2?", num1 is num2)

